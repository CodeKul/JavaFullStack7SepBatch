package controlflowstatement;

import java.util.Scanner;

public class SwitchDemo2 {
    public static void main(String[] args) {
        char alphabet = 'f';
        switch (alphabet){
            case 'a':
                System.out.println("Vowels");
                break;
            case 'i':
                System.out.println("Vowels");
                break;
            case 'e':
                System.out.println("Vowels");
                break;
            case 'o':
                System.out.println("Vowels");
                break;
            case 'u':
                System.out.println("Vowels");
                break;
            default:
                System.out.println("Consonants");
        }

    }
}
